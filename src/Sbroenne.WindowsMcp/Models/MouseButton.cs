namespace Sbroenne.WindowsMcp.Models;

/// <summary>
/// Specifies which mouse button to use for drag operations.
/// </summary>
public enum MouseButton
{
    /// <summary>Left mouse button (default for most drag operations).</summary>
    Left,

    /// <summary>Right mouse button (e.g., for context menu drags).</summary>
    Right,

    /// <summary>Middle mouse button (rare usage).</summary>
    Middle
}
